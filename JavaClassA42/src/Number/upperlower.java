package Number;

public class upperlower {

	public static void main(String[] args) {
		char ch='a';
		 if(ch>='a'&&ch<='z') {
			 System.out.println("is a lower case character  "+ch);
		 }
		 else if(ch>='A'&& ch<='Z')
		 {
			 System.out.println("is a upper case character  "+ch);
		 }
      
	}

}







